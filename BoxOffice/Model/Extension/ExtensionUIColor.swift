import UIKit

extension UIColor {
    /// 플레이스홀더 텍스트 컬러를 반환한다.
    static func placeholderColor() -> UIColor {
        return UIColor(red: 199/255, green: 199/255, blue: 205/255, alpha: 1.0)
    }
}
